<script>
  let scrollpos = window.scrollY
  const header = document.querySelector("#box-menu-category")
  const header_height = header.offsetHeight

  const add_class_on_scroll = () => header.classList.add("fixed-menu-outlets")
  const remove_class_on_scroll = () => header.classList.remove("fixed-menu-outlets")

  window.addEventListener('scroll', function() {
    scrollpos = window.scrollY;

    if (scrollpos >= header_height) {
      add_class_on_scroll()
    } else {
      remove_class_on_scroll()
    }

    console.log(scrollpos)
  })
</script>

<script>
  const getOutletsDataFromAPI = () => {
    const elementOutlets = document.getElementById('getOutletMyAlkes');

    fetch('https://app.myalkes.com/api/v1/outlets', {
        method: "GET",
        header: {
          "Content-Type": "application/json"
        }
      })
      .then((response) => response.json())
      .then((dataOutlets) => {
        dataOutlets.outlets.map((outlet) => {
          elementOutlets.innerHTML += `
            <div class="col-sm-6 col-md-4 py-3">
              <div class="wow fadeInUpShort" data-wow-delay="0s" data-wow-duration="0.4s">
                <div class="card profile">
                  <div class="person">
                    <div class="name">
                      <h4>${outlet.outlet_name}</h4>
                      <p>${outlet.address}</p>
                    </div>
                  </div>
                  <hr class="divider" />
                  <div class="action">
                    <a target="_blank" class="btn secondary waves-effect" href="${outlet.link_map}"><i class="bi bi-geo-alt"></i> Maps</a>
                  </div>
                </div>
              </div>
            </div>
          `
        })
      })
  }
  getOutletsDataFromAPI();
</script>

